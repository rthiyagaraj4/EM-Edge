package eIPAD.chartsummary.clinicalnotes.healthobject;

/**
 * @author vchoughule 
 *
 */
public class ClinicalNotesErrorItem {
	private String errorMessageId;
	private String errorMessage;
	/**
	 * @return the errorMessageId
	 */
	public String getErrorMessageId() {
		return errorMessageId;
	}
	/**
	 * @param errorMessageId the errorMessageId to set
	 */
	public void setErrorMessageId(String errorMessageId) {
		this.errorMessageId = errorMessageId;
	}
	/**
	 * @return the errorMessage
	 */
	public String getErrorMessage() {
		return errorMessage;
	}
	/**
	 * @param errorMessage the errorMessage to set
	 */
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}


}
