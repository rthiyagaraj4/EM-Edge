/**
 * @author saraswathir
 *
 */
public class CLTab {
	
	String MainItem;
	String subItem;
	int subItemPos;
	String date;
	String start;
	String headCode;
	/**
	 * @return the headCode
	 */
	public String getHeadCode() {
		return headCode;
	}
	/**
	 * @param headCode the headCode to set
	 */
	public void setHeadCode(String headCode) {
		this.headCode = headCode;
	}
	/**
	 * @return the start
	 */
	public String getStart() {
		return start;
	}
	/**
	 * @param start the start to set
	 */
	public void setStart(String start) {
		this.start = start;
	}
	/**
	 * @return the mainItem
	 */
	public String getMainItem() {
		return MainItem;
	}
	/**
	 * @param mainItem the mainItem to set
	 */
	public void setMainItem(String mainItem) {
		MainItem = mainItem;
	}
	/**
	 * @return the subItem
	 */
	public String getSubItem() {
		return subItem;
	}
	/**
	 * @param subItem the subItem to set
	 */
	public void setSubItem(String subItem) {
		this.subItem = subItem;
	}
	/**
	 * @return the subItemPos
	 */
	public int getSubItemPos() {
		return subItemPos;
	}
	/**
	 * @param subItemPos the subItemPos to set
	 */
	public void setSubItemPos(int subItemPos) {
		this.subItemPos = subItemPos;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	

}
