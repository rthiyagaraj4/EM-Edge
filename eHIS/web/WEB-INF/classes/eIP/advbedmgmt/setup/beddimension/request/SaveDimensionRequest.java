package eIP.advbedmgmt.setup.beddimension.request;

import eIP.advbedmgmt.common.request.BaseRequest;

/**
 * This class is the request class for saving the dimensions
 * @author GRamamoorthy
 *
 */
public class SaveDimensionRequest extends BaseRequest {

	/**
	 * data members
	 */
	private int width = 0;
	
	private int height = 0;

	/**
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}

	/**
	 * @param width the width to set
	 */
	public void setWidth(int width) {
		this.width = width;
	}

	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}

	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}
	
}
