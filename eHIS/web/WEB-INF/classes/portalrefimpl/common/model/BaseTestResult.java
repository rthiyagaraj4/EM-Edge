package portalrefimpl.common.model;

/**
 * This is the base class for the results module.
 * Contains basic data like order number, result type and order.
 * This class can be extended for storing more details
 * about test results
 * @author GRamamoorthy
 *
 */
public class BaseTestResult {

	/**
	 * data members in this class
	 */
	protected String orderNumber = null;
	
	protected String resultType = null;
	
	protected String order = null;

	/**
	 * @return the orderNumber
	 */
	public String getOrderNumber() {
		return orderNumber;
	}

	/**
	 * @param orderNumber the orderNumber to set
	 */
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}

	/**
	 * @return the resultType
	 */
	public String getResultType() {
		return resultType;
	}

	/**
	 * @param resultType the resultType to set
	 */
	public void setResultType(String resultType) {
		this.resultType = resultType;
	}

	/**
	 * @return the order
	 */
	public String getOrder() {
		return order;
	}

	/**
	 * @param order the order to set
	 */
	public void setOrder(String order) {
		this.order = order;
	}
	
	
	
}
