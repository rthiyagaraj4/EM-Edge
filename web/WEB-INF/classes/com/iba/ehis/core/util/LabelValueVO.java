package com.iba.ehis.core.util;

import com.iba.framework.core.vo.BaseVO;

public class LabelValueVO extends BaseVO {
	private String label;

	private String value;

	public LabelValueVO() {
		super();
	}

	public LabelValueVO(String label, String value) {
		super();
		this.label = label;
		this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
